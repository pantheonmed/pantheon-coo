def parse_accept_language(header: str, allowed: set[str]) -> str | None:
    """Pick first language code from Accept-Language that is in allowed."""
    if not header:
        return None
    for part in header.split(","):
        chunk = part.strip().split(";")[0].strip().lower()
        if "-" in chunk:
            chunk = chunk.split("-", 1)[0]
        if chunk in allowed:
            return chunk
    return None


def t(key: str, lang: str = "en") -> str:
    """Get translation for key in language."""
    lang_dict = TRANSLATIONS.get(lang, TRANSLATIONS["en"])
    return lang_dict.get(key, TRANSLATIONS["en"].get(key, key))


