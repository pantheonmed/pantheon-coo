def prompt_respond_in_language_clause(lang_code: str) -> str:
    """Append to agent system prompts so model outputs match the user's UI language."""
    name = LANGUAGE_CODE_TO_ENGLISH_NAME.get(lang_code, "English")
    return (
        f"\n\nRespond in {name} language. "
        f"All natural-language strings in your JSON (goals, descriptions, notes, criteria, etc.) "
        f"must be written in {name}.\n"
    )


